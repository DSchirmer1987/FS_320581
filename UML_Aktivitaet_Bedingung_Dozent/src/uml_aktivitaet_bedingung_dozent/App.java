package uml_aktivitaet_bedingung_dozent;

public class App {

	public static void main(String[] args) {
		int zahl = 0;
		
		switch(zahl) {
			case 1: {
				System.out.println("Zahl ist eins!");
				break;
			}
			case 2: {
				System.out.println("Zahl ist zwei!");
				break;
			}
			case 3: {
				System.out.println("Zahl ist drei!");
				break;
			}
			default: {
				System.out.println("Unbekannte Zahl!");
				break;
			}
		}

	}

}
